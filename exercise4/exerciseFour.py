"""Ejercicio 4
Dados dos números, mostrar la suma, resta, división y multiplicación de ambos."""
numero1 = float(input("Ingrese primer numero: "))
numero2 = float(input("Ingrese segundo numero: "))

print("La suma de ambos numeros es: " + str(numero1 + numero2))
print("La diferencia de ambos numeros es: " + str(numero1 - numero2))
print("La razon de ambos numeros es: " + str(float(numero1 / numero2)))
print("El producto de ambos numeros es: " + str(numero1 * numero2))