"""Ejercicio 4
Dados dos números, mostrar la suma, resta, división y multiplicación de ambos."""
number1 = float(input("Ingrese primer numero: "))
number2 = float(input("Ingrese segundo numero: "))

print("La suma de ambos numeros es: " + str(number1 + number2))
print("La diferencia de ambos numeros es: " + str(number1 - number2))
print("La razon de ambos numeros es: " + str(float(number1 / number2)))
print("El producto de ambos numeros es: " + str(number1 * number2))