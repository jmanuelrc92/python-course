"""
Exercise 4:

Given two numbers show:
- addition
- subtraction
- division
- multiplication
"""

number1 = float(input("Type first number: "))
number2 = float(input("Type second number: "))

print("Addition: %x" % (number1 + number2))
print("Substraction: %x" % (number1 - number2))
print("Division: %x" % float(number1 / number2))
print("Multiplication: %x" (number1 * number2))